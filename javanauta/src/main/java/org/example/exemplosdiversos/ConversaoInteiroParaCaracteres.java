package org.example.exemplosdiversos;

public class ConversaoInteiroParaCaracteres {

    public static void main(String[] args) {

        int num1 = 79;
        int num2 = 108;
        int num3 = 225;

        char letra1 = (char) num1;
        char letra2 = (char) num2;
        char letra3 = (char) num3;

        System.out.println("Saudação: " + letra1 + letra2 + letra3);


    }
}
