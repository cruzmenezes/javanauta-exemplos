package org.example.logica;

public class ExpressoesComparativas {

    public static void main(String[] args) {

        int pao = 20;
        int acucar = 10;

        System.out.println(pao == acucar);
        System.out.println(pao != acucar);
        System.out.println(pao < acucar);
        System.out.println(pao > acucar);
        System.out.println(pao <= acucar);
        System.out.println(pao >= acucar);
    }
}
